package io.arrogantprogrammer;

public class CastMember {

    String name;

    String character;

    public CastMember(String name, String character) {
        this.name = name;
        this.character = character;
    }

    public CastMember() {
    }

    public String getName() {
        return name;
    }

    public String getCharacter() {
        return character;
    }
}
